package br.com.infnet.tpnuvem.model.negocio;

public class Municipio {
	
	private Integer id;
	private String nome;
	private Estado uf;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public Estado getUf() {
		return uf;
	}
	public void setUf(Estado uf) {
		this.uf = uf;
	}
	
	

	

}
